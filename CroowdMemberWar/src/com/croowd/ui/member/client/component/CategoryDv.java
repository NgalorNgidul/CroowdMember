package com.croowd.ui.member.client.component;

public class CategoryDv {
	Integer type;
	String name;

	public CategoryDv() {

	}

	public CategoryDv(Integer type, String name) {
		this.type = type;
		this.name = name;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return name;
	}

}
